# Create a program to check whether a student passed or failed his/her examination.

# Get integer input marks.
# Check if the mark is greater than or equal to 40.
# If it is greater than or equal to 40, print Pass.
# Else, print Fail.
marks = int(input("Enter the marks:"))
if marks >= 40:
    print("Pass")
else:
    print("Fail")
